/**
 * Custom loading view inspired by [https://semicolonspace.com/jetpack-compose-dialog-loading/](https://semicolonspace.com/jetpack-compose-dialog-loading/)
 *
 * @param[progressIndicatorSize] Size of the progress indicator
 * @param[progressIndicatorColor] Main color for the progress indicator
 */
@Composable
fun SampleCustomLoadingView(
    progressIndicatorSize: Dp = 80.dp,
    progressIndicatorColor: Color = Color(0xFF35898f)
) {

    val infiniteTransition = rememberInfiniteTransition()

    val angle by infiniteTransition.animateFloat(
        initialValue = 0f,
        targetValue = 360f,
        animationSpec = infiniteRepeatable(
            animation = keyframes {
                durationMillis = 600
            }
        )
    )

    CircularProgressIndicator(
        progress = 1f,
        modifier = Modifier
            .size(progressIndicatorSize)
            .rotate(angle)
            .border(
                12.dp,
                brush = Brush.sweepGradient(
                    listOf(
                        Color.White, // add background color first
                        progressIndicatorColor.copy(alpha = 0.1f),
                        progressIndicatorColor
                    )
                ),
                shape = CircleShape
            ),
        strokeWidth = 0.dp,
        color = Color.White // Set background color
    )
}

@Preview
@Composable
fun PreviewSampleCustomLoadingView() {
    Column(modifier = Modifier.fillMaxSize()) {
        SampleCustomLoadingView()
    }
}